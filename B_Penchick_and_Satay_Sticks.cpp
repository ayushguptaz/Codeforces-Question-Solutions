void solve()
{
    int n;
    cin>>n;
    vector<int> v1(n);
    for(int i=0;i<n;i++){
        cin>>v1[i];
    }
    int c1 = 0;
    for(int i=0;i<n;i++){
        if(abs(v1[i]-(i+1))<=1){
            c1++;
        }
    }
    if(c1==n){
        cout<<"YES"<<'\n';
    }
    else{
        cout<<"NO"<<'\n';
    }
    
    




}

    
    


void solve()
{
    int n;
    cin>>n;
    vector<int> v1(n+1);
    for(int i=0;i<n;i++){
        int x;
        cin>>x;
        v1[x]++;
    }
    int ans=0;
    for(int i=0;i<n+1;i++){
        if(v1[i]>=2){
            ans+=(v1[i]/2);

        }
    }
    cout<<ans<<'\n';

    
    
}

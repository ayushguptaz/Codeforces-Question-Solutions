#include <bits/stdc++.h>
using namespace std;
#define int long long
#define all(x) begin(x), end(x)
vector<bool> primes(2*1e5+1,true);
void seive(){
    for(int i=2;i<2*1e5+1;i++){
        for(int j=i*i;j<2*1e5+1;j+=i){
            primes[j]=false;
        }
    }
}
void solve()
{
    int n;
    cin>>n;
    if(n<5){
        cout<<-1<<'\n';
        return;
    }
    
    int i=1;
    vector<int> ans;
    for(i=1;i<=n;i+=2){
        if(i==5){
            continue;
        }
        ans.push_back(i);
    }

    ans.push_back(5);
    ans.push_back(4);
    for(int j=2;j<=n;j+=2){
        if(j==4){
            continue;
        }
        else{
            ans.push_back(j);

        }
    }
    
    for(int i=0;i<n;i++){
        cout<<ans[i]<<" ";
    }
    cout<<'\n';

    
    
}
int32_t main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);
    seive();
    long long t;
    cin >> t;
    for (long long i = 0; i < t; i++)
    {
        solve();
    }

    return 0;
}

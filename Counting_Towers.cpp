#define int long long
#define all(x) begin(x), end(x)
int edit(string s1, string s2, int idx1, int idx2, vector<vector<int>> &dp){
    if(idx1<0 && idx2<0){
        return 0;
    }
    else if(idx1<0 && idx2>=0){
        return idx2+1;
    }
    else if(idx1>=0 && idx2<0 ){
        return idx1+1;
    }
    else{
        if(dp[idx1][idx2]!=-1){
            return dp[idx1][idx2];
        }
        else{
            if(s1[idx1]==s2[idx2]){
                return edit(s1,s2,idx1-1,idx2-1,dp);
            }
            else{
                int t1 = 1 + edit(s1,s2,idx1-1,idx2,dp);
                int t2 = 1 + edit(s1,s2,idx1,idx2-1,dp);
                int t3 = 1 + edit(s1,s2,idx1-1,idx2-1,dp);
                return min(t1,min(t2,t3));
            }
        }
    }
}

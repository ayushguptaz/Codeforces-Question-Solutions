#include <bits/stdc++.h>
using namespace std;
#define int long long
#define all(x) begin(x), end(x)
void solve()
{
    int n,m,L;
    cin>>n>>m>>L;
    vector<vector<int>> hurdles;
    for(int i=0;i<n;i++){
        int l,r;
        cin>>l>>r;
        hurdles.push_back({r-l+2,l});
    }
    vector<vector<int>> power;
    for(int i=0;i<m;i++){
        int x,v;
        cin>>x>>v;
        power.push_back({x,v});
    }
    // for(int i=0;i<n;i++){
    //     cout<<hurdles[i][0]<<" "<<hurdles[i][1]<<'\n';
    // }
    // for(int i=0;i<m;i++){
    //     cout<<power[i][0]<<" "<<power[i][1]<<'\n';
    // }
    int j=0;
    int iniP = 1;
    int cnt = 0;
    priority_queue<int> p1;
    for(int i=0;i<n;i++){
        while(j<m && power[j][0]<hurdles[i][1]){  
            p1.push(power[j][1]);
            j++;
        }
        while(iniP<hurdles[i][0] && p1.size()>0){
            int x = p1.top();
            // cout<<x<<" ";
            p1.pop();
            iniP+=x;
            cnt++;
        }
        if(iniP<hurdles[i][0]){
            cout<<-1<<'\n';
            return;
        }

    }
    cout<<cnt<<" "<<'\n';

    
    
}
int32_t main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    long long t;
    cin >> t;
    for (long long i = 0; i < t; i++)
    {
        solve();
    }

    return 0;
}

#include <bits/stdc++.h>
using namespace std;
#define int long long
#define all(x) begin(x), end(x)
void solve()
{
    int n;
    cin>>n;
    map<int,int> m1;
    for(int i=0;i<n;i++){
        int x;
        cin>>x;
        m1[x]++;
    }
    int maxmf=0;
    for(auto i:m1){
        maxmf = max(i.second,maxmf);
    }
    cout<<n-maxmf<<'\n';

    
    
}
int32_t main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    long long t;
    cin >> t;
    for (long long i = 0; i < t; i++)
    {
        solve();
    }

    return 0;
}

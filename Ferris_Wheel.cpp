#include <bits/stdc++.h>
using namespace std;
#define int long long
#define all(x) begin(x), end(x)
void solve()
{
    int n,x;
    cin>>n>>x;
    vector<int> v1(n);
    for(int i=0;i<n;i++){
        cin>>v1[i];
    }
    sort(all(v1));
    int cnt=0;
    int i=0;
    int j=n-1;
    while(i<=j){
        if(v1[i]+v1[j]<=x){
            i++;
            j--;
            cnt++;
        }
        else{
            j--;
            cnt++;
        
        }
    }
    cout<<cnt;

    
    
}
int32_t main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    long long t=1;
    // cin >> t;
    for (long long i = 0; i < t; i++)
    {
        solve();
    }

    return 0;
}
